package exemplos;

public class Exemplo03 {
    public static void main(String[] args) {
        int n1, n2;

        n1 = 5;
        n2 = 7;
        
        // operador aritmético gera um número
        System.out.println(n1 + n2);

        // Operador relacional sempre gera true ou false
        System.out.println( n1 > n2 );
        System.out.println( n1 <= n2 );
        System.out.println( n1 == n2 );
        System.out.println( n1 != n2 );

    }
}
