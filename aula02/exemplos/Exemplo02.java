package exemplos;

public class Exemplo02 {
    public static void main(String[] args) {
        System.out.println("Bom dia!"); // ln = new line, nova linha
        System.out.println("2 + 3");
        System.out.println(2 + 3); // + = soma
        System.out.println("Olá" + "! Boa tarde."); // + = concatenação (juntar)
        System.out.println("2 + 3 = " + 2 + 3);
        System.out.println("2 + 3 = " + (2 + 3));

        //Operadores:
        // + - * /
    }
}
