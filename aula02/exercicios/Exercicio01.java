package exercicios;

/* Fazer as 4 operações básicas (+, -, *, /) entre os valores 15 e 7 */

// ALT + SHIF + SETA = copia linha

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("15 + 7 = " + (15 + 7));
        System.out.println("15 - 7 = " + (15 - 7));
        System.out.println("15 * 7 = " + (15 * 7));
        System.out.println("15 / 7 = " + (15.0 / 7));
    }
}
